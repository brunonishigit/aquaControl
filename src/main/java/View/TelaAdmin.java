    private void excluirButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_excluirButtonActionPerformed
        // TODO add your handling code here:
         int linha = TabelaUsuarios.getSelectedRow();

    if (linha == -1) {
        JOptionPane.showMessageDialog(this, "Selecione um usuário para excluir.");
        return;
    }

    int confirmar = JOptionPane.showConfirmDialog(this, "Tem certeza que deseja excluir esse usuário?", "Confirmar Exclusão", JOptionPane.YES_NO_OPTION);

    if (confirmar == JOptionPane.YES_OPTION) {
        try {
            int id = (int) TabelaUsuarios.getValueAt(linha, 0); // ID está na coluna 0
            Connection con = ConnectFactory.obtemConexao();
            String sql = "DELETE FROM usuario WHERE id = ?";
            PreparedStatement ps = con.prepareStatement(sql);
            ps.setInt(1, id);
            ps.executeUpdate();
            con.close();

            JOptionPane.showMessageDialog(this, "Usuário excluído com sucesso.");
            carregarUsuarios(); // Atualiza tabela
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Erro ao excluir usuário: " + e.getMessage());
        }
    }
    }//GEN-LAST:event_excluirButtonActionPerformed

    private void editarButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_editarButtonActionPerformed
        int linha = TabelaUsuarios.getSelectedRow();

        if (linha == -1) {
            JOptionPane.showMessageDialog(this, "Selecione um usuário para editar.");
            return;
            }

        int id = (int) TabelaUsuarios.getValueAt(linha, 0);
        String nome = (String) TabelaUsuarios.getValueAt(linha, 1);
        String tipo = (String) TabelaUsuarios.getValueAt(linha, 2);

        Usuario usuarioSelecionado = new Usuario();
        usuarioSelecionado.setId(id);   
        usuarioSelecionado.setNome(nome);
        usuarioSelecionado.setTipo(tipo);
        
        EditarUsuarioForm editarForm = new EditarUsuarioForm(usuarioSelecionado);
        
        // Após fechar o diálogo, recarregue a tabela:
        editarForm.addWindowListener(new WindowAdapter() {
            @Override
            public void windowClosed(WindowEvent e) {
                carregarUsuarios();
                }
            });

    editarForm.setVisible(true);
    this.dispose();
    }//GEN-LAST:event_editarButtonActionPerformed

