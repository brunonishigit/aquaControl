     private void carregarUsuarios() {
        DefaultTableModel modelo = (DefaultTableModel) TabelaUsuarios.getModel();
        modelo.setRowCount(0);

        try {
            Connection con = ConnectFactory.obtemConexao();
            String sql = "SELECT id, nome, tipo FROM usuario";
            PreparedStatement ps = con.prepareStatement(sql);
            ResultSet rs = ps.executeQuery();

            while (rs.next()) {
                int id = rs.getInt("id");
                String nome = rs.getString("nome");
                String tipo = rs.getString("tipo");
                
                modelo.addRow(new Object[]{id, nome, tipo});
            }

            con.close();
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Erro ao carregar usuários: " + e.getMessage());
        }
    }



